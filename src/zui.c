{
	cmd_cnt = 0;
}

static void* zui_obj_get(zui_obj_id_t id)
{
	WLU_ASSERT(id < objs_idxs_cnt);
	void* ptr = objs_buffer + objs_idxs[id];
	WLU_ASSERT((((uintptr_t)ptr) % MAX_ALIGNMENT_SIZE) == 0);
	return objs_buffer + objs_idxs[id];
}

static zui_obj_id_t zui_obj_push(int size)
{
	size = get_next_mem_aligned_value(size);

	WLU_ASSERT(objs_buffer_size + size <= ZUI_OBJS_BUFFER_MAX_SIZE);
	WLU_ASSERT(objs_idxs_cnt < ZUI_MAX_OBJS);
	zui_obj_id_t id = objs_idxs_cnt++;
	objs_idxs[id] = objs_buffer_size;
	objs_buffer_size += size;
	memset(zui_obj_get(id), 0, size);

	return id;
}

static void zui_obj_resize(zui_obj_id_t id, int new_size)
{
	WLU_ASSERT(id < objs_idxs_cnt);

	new_size = get_next_mem_aligned_value(new_size);

	const int obj_index = objs_idxs[id];

	/* 
		last obj in buffer:
			expand/shrink buffer size
	*/
	if (id == objs_idxs_cnt - 1) {
		const int old_size = objs_buffer_size - obj_index;
	
		if (new_size == old_size)
			return;

		const int size_diff = new_size - old_size;
		
		objs_buffer_size += size_diff;

		WLU_ASSERT(objs_buffer_size < ZUI_OBJS_BUFFER_MAX_SIZE);

		return;
	}

	/* 
		NOT last obj in buffer:
			move adjacent data forward/backwards
			set adjacent objs_idxs to new values
	*/

	const int next_obj_index = objs_idxs[id + 1];
	const int old_size = next_obj_index - obj_index;
	
	if (new_size == old_size)
		return;

	const int size_diff = new_size - old_size;


	WLU_ASSERT((objs_buffer_size + size_diff) < ZUI_OBJS_BUFFER_MAX_SIZE);

	memmove(
		objs_buffer + next_obj_index + size_diff,
		objs_buffer + next_obj_index,
		objs_buffer_size - next_obj_index
	);

	for (int i = id + 1; i < objs_idxs_cnt; ++i) {
		objs_idxs[i] += size_diff;
	}

	objs_buffer_size += size_diff;
}

static void zui_obj_clear(void)
{
	objs_buffer_size = 0;
	objs_idxs_cnt = 0;
}

static struct vec2i get_text_require_buffer_size_ex(int linelen, int lines)
{
	const int w =  (linelen * CHAR_W) + linelen;
	const int h = (lines * CHAR_H) + lines;
	return (struct vec2i) { .x = w, .y = h };
}

static struct vec2i get_text_required_buffer_size(const char* str)
{
	const int linelen = str_longest_line_len(str);
	const int lines = 1 + str_chr_cnt(str, '\n');
	return get_text_require_buffer_size_ex(linelen, lines);
}

static int get_text_max_str_size(int linelen, int lines)
{
	return (linelen * lines) + lines;
}

static void clean_rect(struct recti rect, struct rgb24* fb)
{
	for (int i = 0; i < rect.size.y; ++i) {
		const int offset = ((rect.coord.y + i) * ZUI_WIDTH) + rect.coord.x;
		memset(&fb[offset], 0x11, rect.size.x * 3);
	}
}

static void clean_buffer(struct rgb24* fb)
{
	memset(fb, 0x11, ZUI_WIDTH * ZUI_HEIGHT * 3);
}

static void draw_borders(struct rgb24* dest) 
{
	const struct rgb24* hbord = (void*) hborder_data;
	const struct rgb24* vbord = (void*) vborder_data;
	const int w = ZUI_WIDTH;
	const int h = ZUI_HEIGHT;

	for (int i = 0; i < HBORDER_H; ++i) {
		for (int j = 0; j < w; ++j) {
			dest[i * w + j] = hbord[(i * HBORDER_W) + (j % HBORDER_W)];
			dest[((w * h) - HBORDER_H * w) + i * w + j] = hbord[(i * HBORDER_W) + (j % HBORDER_W)];
		}
	}

	for (int i = 0; i < h; ++i) {
		for (int j = 0; j < VBORDER_W; ++j) {
			dest[i * w + j] = vbord[((i % VBORDER_H) * VBORDER_W) + j];
			dest[(w - VBORDER_W) + i * w + j] = vbord[((i % VBORDER_H) * VBORDER_W) + j];
		}
	}
}

